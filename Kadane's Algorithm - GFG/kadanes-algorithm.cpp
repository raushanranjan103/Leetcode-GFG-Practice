class Solution{
    public:
    // arr: input array
    // n: size of array
    //Function to find the sum of contiguous subarray with maximum sum.
    long long maxSubarraySum(int arr[], int n){
        long long int sum1=0;
        long long int res=INT_MIN;
        
        for(long long int i=0;i<n;i++)
        { sum1+=arr[i];
          res=max(res,sum1);
          if(sum1<0)
          {
              sum1=0;
          }
        }
        
        
        return res;
        //else
        //return x;
        
        
    }
};

